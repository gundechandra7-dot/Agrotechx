function getAdviceTranslation(lang, adviceEn, list) {
  const row = list.find((r) => r.en === adviceEn);
  if (!row) return adviceEn;
  return row[lang] || row.en;
}

export function getResultText(lang, category, value) {
  if (value == null || value === "") return value;
  const langMap = resultByLang[lang] || resultByLang.en;
  const fallback = resultByLang.en;
  if (category === "weatherCondition") {
    return langMap.weatherCondition?.[value] ?? fallback.weatherCondition?.[value] ?? value;
  }
  if (category === "diseaseName") {
    return langMap.diseaseName?.[value] ?? fallback.diseaseName?.[value] ?? value;
  }
  if (category === "severity") {
    return langMap.severity?.[value] ?? fallback.severity?.[value] ?? value;
  }
  if (category === "cropName") {
    return langMap.cropName?.[value] ?? fallback.cropName?.[value] ?? value;
  }
  if (category === "diseaseAdvice") {
    return getAdviceTranslation(lang, value, resultByLang.en.diseaseAdvice);
  }
  if (category === "cropAdvice") {
    return getAdviceTranslation(lang, value, resultByLang.en.cropAdvice);
  }
  return value;
}
